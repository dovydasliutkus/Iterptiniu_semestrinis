void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin == GPIO_PIN_8)
	{
		status = DATA_READY;
		suvis = suvis + 1;
		suvis_total = suvis_total + 1;
		HAL_TIM_Base_Start_IT(&htim6);
		HAL_NVIC_DisableIRQ(EXTI4_15_IRQn);
	}
	if(GPIO_Pin == GPIO_PIN_13)
	{
		status = DATA_RESET;
		suvis = 0;
		pertaisymai++;
	}
}
